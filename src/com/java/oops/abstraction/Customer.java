package com.java.oops.abstraction;

public class Customer {
   int id;
   String name;
   int age;
   String phoneNo;
   String email;

    public Customer(int id, String name, int age, String phoneNo, String email) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.phoneNo = phoneNo;
        this.email = email;
    }
}
